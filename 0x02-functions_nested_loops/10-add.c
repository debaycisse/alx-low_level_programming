/**
 * add - adds two integer together
 * @n1: the first parameter to be added to n2.
 * @n2: the second parameter to be added to n1.
 *
 * Description: adds two integer together and return their sum's value
 * Return: returns the total of the two.
 */
int add(int n1, int n2)
{
	return (n1 + n2);
}
